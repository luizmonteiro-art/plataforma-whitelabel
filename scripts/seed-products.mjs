// ── Execução ──────────────────────────────────────────────────────────────────
async function seed() {
  console.log(`\n🌱 Iniciando seed — ${products.length} produtos para store_id="${STORE_ID}"`)

  const { data, error } = await supabase
    .from('products')
    .upsert(products, { onConflict: 'store_id,slug', ignoreDuplicates: false })
    .select('id, name')

  if (error) {
    console.error('\n❌ Erro ao inserir produtos:', error.message)
    console.error(error)
    process.exit(1)
  }

  console.log(`\n✅ ${data?.length ?? 0} produtos inseridos/atualizados com sucesso!\n`)
  data?.forEach((p, i) => console.log(`  ${String(i + 1).padStart(2, '0')}. ${p.name}`))
  console.log('\n🎉 Seed concluído! Acesse /admin/estoque para ver os produtos.\n')
}

